/**
 * Vibrant gradient palettes for flashcard backgrounds.
 * Cycles through these gradients based on card index.
 */
export const GRADIENT_PALETTES = [
  'linear-gradient(135deg, #667eea 0%, #764ba2 100%)',
  'linear-gradient(135deg, #f093fb 0%, #f5576c 100%)',
  'linear-gradient(135deg, #4facfe 0%, #00f2fe 100%)',
  'linear-gradient(135deg, #43e97b 0%, #38f9d7 100%)',
  'linear-gradient(135deg, #fa709a 0%, #fee140 100%)',
  'linear-gradient(135deg, #a8edea 0%, #fed6e3 100%)',
];

/**
 * Returns the gradient for a given index, cycling through palettes.
 */
export function getGradient(index: number): string {
  return GRADIENT_PALETTES[index % GRADIENT_PALETTES.length];
}

